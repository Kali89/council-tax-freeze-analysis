"""
Maps each 2025-vintage English LAD code to the group of LAs it shares its
non-district precepting tiers with (county council, or metropolitan-county
police/fire, or the GLA) - used only to compute
`engine.build.compute_single_pot_bias_risk`'s divergence statistic, never
the counterfactual liability itself.

Source: ONS Open Geography Portal, "Local Authority District to County
(December 2024) Lookup in EN" (ArcGIS item 7b21cc353fe940e9b0e05442830939ab,
FeatureServer LAD24_CTY24_EN_LU), queried directly - same sourcing pattern
as lad_2025.py. Fields LAD24CD/CTY24NM give each two-tier shire district's
county, each metropolitan district's metropolitan county, and each London
borough's Inner/Outer London designation.

Two adjustments made to the raw ONS table, both cheap and both documented
here rather than silently baked in:

1. "Inner London" and "Outer London" are merged into one "Greater London"
   group. The lookup's own inner/outer split reflects a different, older
   administrative distinction; the GLA/MPS/LFB precept this statistic cares
   about is set once, uniformly, for the whole of Greater London, not
   separately for inner and outer boroughs.
2. Barnsley and Sheffield are keyed under their 2025 codes (E08000038,
   E08000039) via the same alias already used for the CTSOP join in
   engine/build.py - the Dec-2024 source table predates their 2025 SPLIT
   and still uses the old codes (E08000016, E08000019).

The remaining 63 LADs are standalone unitary authorities the ONS lookup
does not assign to any county (they ARE their own county-equivalent for
local government purposes: Cornwall, County Durham, Wiltshire, Hartlepool,
etc.) - each is mapped to a group containing only itself
(f"__standalone__{code}"). This is a real, documented gap, not an
oversight: for these 63 LAs, `compute_single_pot_bias_risk` cannot measure
divergence from a true shared-tier peer group (their real peers for
police/fire purposes cross county lines this lookup doesn't capture - e.g.
Cumbria Constabulary still covers both Cumberland and Westmorland and
Furness post-2023-split) and reports divergence as unmeasured (NaN) rather
than silently defaulting to "no divergence". Their EXPOSURE
(shared_tier_share) is unaffected by this gap and remains a real, computed
bound: however much an unmeasured peer group might diverge, the shared-tier
component - whatever its true rate - can be at most shared_tier_share of
the whole bill.
"""

